"use strict"

class Calculator{
    add(x,y){
        return x + y
    }
    subtract(x,y){
        return x - y
    }
    divide(x,y){
        return x / y
    }
    multiply(x,y){
        return x * y
    }
    squareRoot(x){
        return Math.sqrt(x)
    }
    cubicRoot(x){
        return Math.cbrt(x)
    }
    powerByX(x, y){
        return x**y
    }

}